(function () {
    //?[NEW]
    Game_Event.prototype.isABSEvent = function () {
        try {
            var ev = this.event();
            if (ev.meta && ev.meta.ABS) {
                return true;
            }
        } catch (error) {
            console.error(error);
            return false;
        }
        return false;
    };
})();
