    Sprite_Button.prototype.isHoverByMouse = function () {
        var x = this.canvasToLocalX(TouchInput.mX);
        var y = this.canvasToLocalY(TouchInput.mY);
        return x >= 0 && y >= 0 && x < this.width && y < this.height;
    };
